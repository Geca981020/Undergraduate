#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
	
	int i = 100;
	int j = 48 ;
	
	printf("i >  j = %d \n", i>j);
	printf("i >= j = %d \n", i>=j);
	printf("i <  j = %d \n", i<j);
	printf("i <= j = %d \n", i<=j);
	printf("i == j = %d \n", i==j);
	printf("i != j = %d \n", i!=j);
}
