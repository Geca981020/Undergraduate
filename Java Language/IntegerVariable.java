package JavaStudy;

public class IntegerVariable {
	public static void main(String[] args) {
		byte a = 127;
		short b = 32767;
		int c = 2147483647;
		long d = 11111111111L;
		System.out.println(a+b+c+d);
		char ch1 = 'a';
		char ch2 = 'z';
		char ch3 = '\u004f';	
		System.out.println("output =" +ch1+ch2+ch3);
	}
}
